"""Final coverage report generator using llvm-cov.

This script is invoked by Bazel as the --coverage_report_generator after all tests
complete. It reads the per-test zip files produced by the merger, merges all profdata
into one, and generates the final combined HTML report.

Expected Bazel interface:
    --reports_file=<path>    Text file listing paths to all per-test coverage outputs
    --output_file=<path>     Where to write the final report (zip)
"""

import argparse
import json
import os
import re
import subprocess
import sys
import zipfile
from pathlib import Path
from typing import List, Set, Tuple
from python.runfiles import Runfiles


def main() -> None:
    """Main entry point."""
    args = parse_args()

    # Read the list of per-test report files.
    reports = read_reports_file(args.reports_file)
    if not reports:
        print("INFO: No coverage reports found.", file=sys.stderr)
        write_empty_output(args.output_file)
        sys.exit(0)

    # Extract profdata and object files from each per-test zip.
    valid_profdata_files, valid_object_files = extract_reports(reports)

    if not valid_profdata_files or not valid_object_files:
        print("INFO: No valid profdata or object files found.", file=sys.stderr)
        write_empty_output(args.output_file)
        sys.exit(0)

    # Get llvm tools via runfiles. The rlocation paths are passed in by the
    # wrapper script (generated by score_coverage_reporter macro) so the
    # llvm toolchain repository can be named anything the consumer prefers.
    r = Runfiles.Create()
    llvm_cov_resolved = r.Rlocation(args.llvm_cov)
    if not llvm_cov_resolved:
        print(f"ERROR: llvm-cov not found in runfiles via {args.llvm_cov}", file=sys.stderr)
        sys.exit(1)
    llvm_bin_path = Path(llvm_cov_resolved)

    llvm_profdata_resolved = r.Rlocation(args.llvm_profdata)
    if not llvm_profdata_resolved:
        print(f"ERROR: llvm-profdata not found in runfiles via {args.llvm_profdata}", file=sys.stderr)
        sys.exit(1)

    # Merge all profdata files.
    merged_profdata = Path.cwd() / "merged_coverage.profdata"
    run_command([
        llvm_profdata_resolved, "merge",
        "--sparse",
        "--output", str(merged_profdata),
    ] + sorted(valid_profdata_files))

    # Build coverage arguments.
    coverage_args = ["--instr-profile", str(merged_profdata)]
    for obj in sorted(valid_object_files):
        coverage_args.extend(["--object", obj])

    # Get filter regexes and workspace root.
    filter_regexes = load_filter_regexes(r, args.filter_regexes)
    workspace_root = args.workspace_root

    common_show_args = {
        "llvm_bin_path": llvm_bin_path,
        "coverage_args": coverage_args,
        "filter_regexes": sorted(filter_regexes),
        "workspace_root": workspace_root,
    }

    # Generate HTML report.
    html_report_dir = Path.cwd() / "html_report"
    run_llvm_cov_show(
        **common_show_args,
        output_format="html",
        html_report_dir=html_report_dir,
    )
    lcov_report_dir = Path.cwd() / "lcov_report"
    lcov_report_dir.mkdir(exist_ok=True)
    lcov_result = run_llvm_cov_export(
        llvm_bin_path=llvm_bin_path,
        coverage_args=coverage_args,
        filter_regexes=sorted(filter_regexes),
        workspace_root=workspace_root,
    )
    lcov_text = lcov_result.stdout

    # Augment with 0%-coverage entries for files that no test linked against.
    untested_sources: List[str] = []
    if args.instrumented_sources_manifest:
        manifest_path = r.Rlocation(args.instrumented_sources_manifest)
        if not manifest_path or not Path(manifest_path).exists():
            print(
                f"WARNING: instrumented sources manifest not found via "
                f"{args.instrumented_sources_manifest}",
                file=sys.stderr,
            )
        else:
            covered = _covered_sources_from_lcov(lcov_text)
            untested_sources = _find_untested_sources(
                manifest_path=Path(manifest_path),
                workspace_root=workspace_root,
                covered_sources=covered,
                filter_regexes=sorted(filter_regexes),
            )
            if untested_sources:
                print(
                    f"INFO: Augmenting report with {len(untested_sources)} "
                    f"untested source file(s).",
                    file=sys.stderr,
                )
                lcov_text = _append_zero_coverage_lcov(
                    lcov_text, untested_sources, workspace_root
                )

    with open(lcov_report_dir / "lcov.dat", "w", encoding="utf-8") as f:
        f.write(lcov_text)

    # Augment the HTML report with 0%-coverage pages for untested files.
    if untested_sources:
        _augment_html_with_untested(
            html_report_dir=html_report_dir,
            untested_sources=untested_sources,
            workspace_root=workspace_root,
        )

    # Generate text summary.
    text_report_dir = Path.cwd() / "text_report"
    text_report_dir.mkdir(exist_ok=True)
    summary = run_llvm_cov_report(
        llvm_bin_path=llvm_bin_path,
        coverage_args=coverage_args,
        filter_regexes=sorted(filter_regexes),
    )
    summary_text = summary.stdout
    if untested_sources:
        summary_text = _augment_text_summary(summary_text, untested_sources)
    with open(text_report_dir / "summary.txt", "w", encoding="utf-8") as f:
        f.write(summary_text)
    print(summary_text, file=sys.stderr)

    # Package everything into the output zip.
    directories = [html_report_dir, lcov_report_dir, text_report_dir]
    create_zip(
        root=Path.cwd(),
        directories=directories,
        output_file=args.output_file,
    )

    print(f"INFO: Coverage reporter completed. Output: {args.output_file}", file=sys.stderr)


def run_llvm_cov_show(
    llvm_bin_path: Path,
    coverage_args: List[str],
    filter_regexes: List[str],
    workspace_root: str,
    output_format: str,
    html_report_dir: Path = None,
) -> subprocess.CompletedProcess:
    """Run llvm-cov show."""
    cmd = [
        str(llvm_bin_path),
        "show",
        f"--format={output_format}",
        f"--path-equivalence=/proc/self/cwd/,{workspace_root}",
        f"--compilation-dir={workspace_root}",
        "--show-branches=count",
        "--show-region-summary=0",
    ]

    cxxfilt = llvm_bin_path.parent / "llvm-cxxfilt"
    if cxxfilt.exists():
        cmd.append(f"--Xdemangler={cxxfilt}")

    for regex in filter_regexes:
        adjusted = regex.replace("/proc/self/cwd/", workspace_root)
        cmd.append(f"--ignore-filename-regex={adjusted}")

    if html_report_dir:
        cmd.append(f"--output-dir={html_report_dir}")
        cmd.append("--coverage-watermark=100,50")
        cmd.append("--show-expansions")

    cmd.extend(coverage_args)
    return run_command(cmd)


def run_llvm_cov_export(
    llvm_bin_path: Path,
    coverage_args: List[str],
    filter_regexes: List[str],
    workspace_root: str,
) -> subprocess.CompletedProcess:
    """Run llvm-cov export to produce LCOV format."""
    cmd = [
        str(llvm_bin_path),
        "export",
        "--format=lcov",
        f"--path-equivalence=/proc/self/cwd/,{workspace_root}",
        f"--compilation-dir={workspace_root}",
    ]

    for regex in filter_regexes:
        adjusted = regex.replace("/proc/self/cwd/", workspace_root)
        cmd.append(f"--ignore-filename-regex={adjusted}")

    cmd.extend(coverage_args)
    return run_command(cmd, separate_stderr=True)


def run_llvm_cov_report(
    llvm_bin_path: Path,
    coverage_args: List[str],
    filter_regexes: List[str],
) -> subprocess.CompletedProcess:
    """Run llvm-cov report for a summary."""
    cmd = [
        str(llvm_bin_path),
        "report",
        "--summary-only",
        "--show-region-summary=0",
        "--show-branch-summary=1",
    ]

    for regex in filter_regexes:
        cmd.append(f"--ignore-filename-regex={regex}")

    cmd.extend(coverage_args)
    return run_command(cmd, separate_stderr=True)


def extract_reports(reports: List[str]) -> Tuple[Set[str], Set[str]]:
    """Extract profdata and object files from per-test zip files."""
    valid_profdata_files = set()
    valid_object_files = set()

    for i, report_path in enumerate(reports):
        # Skip baseline_coverage files (LCOV format, not our zip).
        if "baseline_coverage" in report_path:
            continue

        report = Path(report_path)
        if not report.exists() or report.stat().st_size == 0:
            continue

        # Check if it's a valid zip.
        if not zipfile.is_zipfile(report):
            continue

        profdata_name = f"coverage_report_{i:08d}.profdata"

        try:
            with zipfile.ZipFile(report, "r") as archive:
                # Extract meta.
                meta_json = archive.read("meta/meta.json")
                target_meta = json.loads(meta_json)

                # Extract profdata.
                profdata_content = archive.read("profdata/target.profdata")
                profdata_path = Path.cwd() / profdata_name
                with open(profdata_path, "wb") as f:
                    f.write(profdata_content)

                valid_profdata_files.add(str(profdata_path))

                # Collect object files.
                for obj in target_meta.get("object_files", []):
                    if obj and Path(obj).exists():
                        valid_object_files.add(os.path.realpath(obj))

        except (zipfile.BadZipFile, KeyError, json.JSONDecodeError) as e:
            print(f"WARNING: Skipping invalid report {report_path}: {e}", file=sys.stderr)
            continue

    return valid_profdata_files, valid_object_files

def read_reports_file(reports_file: Path) -> List[str]:
    """Read the reports file listing all per-test coverage outputs."""
    with open(reports_file, encoding="utf-8") as f:
        return [line.strip() for line in f if line.strip()]


def load_filter_regexes(runfiles: Runfiles, rlocation_path: str) -> List[str]:
    """Load filter regexes from filter_regexes.txt via Bazel runfiles."""
    path = runfiles.Rlocation(rlocation_path)
    if not path or not Path(path).exists():
        print(f"WARNING: {rlocation_path} not found in runfiles, no source filtering applied",
              file=sys.stderr)
        return []

    lines = Path(path).read_text(encoding="utf-8").splitlines()
    return [line.strip() for line in lines if line.strip() and not line.strip().startswith("#")]


def write_empty_output(output_file: Path) -> None:
    """Write an empty file as output when there's nothing to report."""
    with open(output_file, "w", encoding="utf-8") as f:
        f.write("")


def run_command(cmd: List[str], separate_stderr: bool = False) -> subprocess.CompletedProcess:
    """Run a command and exit on failure.

    When separate_stderr=True, stderr is captured separately from stdout so
    that callers which parse stdout as structured data (e.g. LCOV text) are
    not polluted by llvm-cov warning messages.
    """
    stderr_target = subprocess.PIPE if separate_stderr else subprocess.STDOUT
    try:
        return subprocess.run(
            cmd,
            check=True,
            stdout=subprocess.PIPE,
            stderr=stderr_target,
            text=True,
        )
    except subprocess.CalledProcessError as e:
        print(f"ERROR: Command failed with code {e.returncode}:", file=sys.stderr)
        print(f"  {' '.join(cmd)}", file=sys.stderr)
        output = (e.stdout or "") + (e.stderr or "")
        if output:
            print(output, file=sys.stderr)
        sys.exit(1)


def create_zip(root: Path, directories: List[Path], output_file: Path) -> None:
    """Create a zip file from the given directories relative to root."""
    with zipfile.ZipFile(output_file, "w", zipfile.ZIP_DEFLATED) as zf:
        for directory in directories:
            if not directory.exists():
                continue
            for dirpath, _, files in os.walk(directory):
                for filename in files:
                    file_path = Path(dirpath) / filename
                    arcname = file_path.relative_to(root)
                    zf.write(file_path, arcname)


def _covered_sources_from_lcov(lcov_text: str) -> Set[str]:
    """Return the set of absolute source paths that appear in an LCOV report."""
    sources: Set[str] = set()
    for line in lcov_text.splitlines():
        if line.startswith("SF:"):
            sources.add(os.path.realpath(line[3:].strip()))
    return sources


def _find_untested_sources(
    manifest_path: Path,
    workspace_root: str,
    covered_sources: Set[str],
    filter_regexes: List[str],
) -> List[str]:
    """Read the manifest and return entries not present in covered_sources.

    Manifest entries are workspace-relative paths. Filter regexes from the
    consumer are applied so that the same exclusions that affect llvm-cov also
    affect the synthesized entries. Entries that do not resolve to an existing
    file on disk are dropped silently (typically generated files or stale
    manifest content).
    """
    ws = Path(workspace_root)
    compiled_filters = [re.compile(r) for r in filter_regexes if r]

    ws_resolved = ws.resolve()
    untested: List[str] = []
    seen: Set[str] = set()
    raw = manifest_path.read_text(encoding="utf-8")
    for entry in raw.splitlines():
        rel = entry.strip()
        if not rel:
            continue
        abs_path = (ws / rel).resolve()
        if not abs_path.is_relative_to(ws_resolved):
            continue
        if not abs_path.exists() or not abs_path.is_file():
            continue
        abs_str = str(abs_path)
        if abs_str in covered_sources or abs_str in seen:
            continue
        if any(rx.search(abs_str) or rx.search(rel) for rx in compiled_filters):
            continue
        seen.add(abs_str)
        untested.append(abs_str)
    return sorted(untested)


_NON_EXECUTABLE_RE = re.compile(
    r"^\s*("
    r"[{}]"                        # lone braces
    r"|//.*"                       # line comments
    r"|/\*.*"                      # block comment open
    r"|\*(?:[/\s].*)?"              # block comment continuation (* text) / close (*/), lone *
    r"|\*/.*"                      # block comment close
    r"|#\s*(?:include|define|undef|if|ifdef|ifndef|elif|else|endif|pragma|error|warning)\b.*"
    r"|namespace\b[^{;]*[{]?\s*"   # namespace declarations
    r"|}\s*//.*"                    # closing brace with comment
    r"|}\s*namespace.*"            # closing namespace
    r"|public\s*:|private\s*:|protected\s*:"
    r")\s*$"
)


def _is_likely_executable(line: str) -> bool:
    """Heuristic: return True if a line is likely an executable C/C++ statement."""
    stripped = line.strip()
    if not stripped:
        return False
    return _NON_EXECUTABLE_RE.match(line) is None


def _count_instrumentable_lines(path: str) -> Tuple[List[int], int]:
    """Return (list of 1-based likely-executable line numbers, count).

    This is a conservative approximation. Without a real parser we cannot
    perfectly distinguish executable from non-executable lines. The heuristic
    excludes blank lines, comments, preprocessor directives, lone braces,
    namespace declarations, and access specifiers.
    """
    line_numbers: List[int] = []
    try:
        with open(path, "r", encoding="utf-8", errors="replace") as f:
            for i, line in enumerate(f, 1):
                if _is_likely_executable(line):
                    line_numbers.append(i)
    except OSError:
        pass
    return line_numbers, len(line_numbers)


def _append_zero_coverage_lcov(
    lcov_text: str, untested_sources: List[str], workspace_root: str
) -> str:
    """Append minimal zero-coverage LCOV records for each untested source."""
    blocks: List[str] = []
    for abs_path in untested_sources:
        line_numbers, lf = _count_instrumentable_lines(abs_path)
        if lf == 0:
            continue
        da = "\n".join(f"DA:{n},0" for n in line_numbers)
        block = (
            f"SF:{abs_path}\n"
            f"{da}\n"
            f"LF:{lf}\n"
            f"LH:0\n"
            "end_of_record\n"
        )
        blocks.append(block)
    if not blocks:
        return lcov_text
    sep = "" if lcov_text.endswith("\n") else "\n"
    return lcov_text + sep + "".join(blocks)


def _augment_text_summary(summary_text: str, untested_sources: List[str]) -> str:
    """Re-compute the TOTALS line in the llvm-cov report summary.

    llvm-cov report --summary-only emits a table ending with a TOTALS row:

        TOTAL  <regions> <miss> <cover> <functions> ... <lines> <miss> <cover> <branches> ...

    We parse the existing totals, add the untested file line counts, and
    rewrite the TOTALS row so that summary.txt and the CI console reflect the
    augmented numbers.
    """
    extra_lines_found = 0
    for abs_path in untested_sources:
        _, lf = _count_instrumentable_lines(abs_path)
        extra_lines_found += lf

    if extra_lines_found == 0:
        return summary_text

    lines = summary_text.splitlines(keepends=True)
    totals_idx = None
    for i in range(len(lines) - 1, -1, -1):
        if lines[i].strip().startswith("TOTAL"):
            totals_idx = i
            break

    if totals_idx is None:
        banner = (
            f"\n[score-coverage] {len(untested_sources)} file(s) not linked "
            f"into any test ({extra_lines_found} lines at 0% coverage) — "
            f"not reflected in the TOTALS above.\n"
        )
        return summary_text + banner

    totals_line = lines[totals_idx]

    # Determine which numeric triple corresponds to "Lines" by inspecting the
    # column header (the line immediately above TOTAL).  llvm-cov report emits
    # headers like "Filename  Function  Line  Branch  ...".  We find the
    # column position of "Line" in the header and match it to the correct
    # numeric group in the TOTALS row.
    lines_group_idx = None
    if totals_idx > 0:
        header_line = lines[totals_idx - 1]
        header_cols = re.finditer(r"\b(Regions?|Functions?|Lines?|Branches?)\b", header_line)
        for col_idx, col_match in enumerate(header_cols):
            if col_match.group().startswith("Line"):
                lines_group_idx = col_idx
                break

    pct_groups = list(re.finditer(r"(\d+)(\s+)(\d+)(\s+)([\d.]+%)", totals_line))

    if lines_group_idx is not None and lines_group_idx < len(pct_groups):
        m = pct_groups[lines_group_idx]
        try:
            old_lf = int(m.group(1))
            old_lm = int(m.group(3))
            new_lf = old_lf + extra_lines_found
            new_lm = old_lm + extra_lines_found
            new_pct = ((new_lf - new_lm) / new_lf * 100) if new_lf > 0 else 0

            old_lf_str = m.group(1)
            old_lm_str = m.group(3)
            old_pct_str = m.group(5)
            new_lf_str = str(new_lf).rjust(len(old_lf_str))
            new_lm_str = str(new_lm).rjust(len(old_lm_str))
            new_pct_str = f"{new_pct:.2f}%".rjust(len(old_pct_str))

            replacement = (
                new_lf_str + m.group(2) + new_lm_str + m.group(4) + new_pct_str
            )
            lines[totals_idx] = (
                totals_line[:m.start()] + replacement + totals_line[m.end():]
            )
            return "".join(lines)
        except (ValueError, IndexError):
            pass

    banner = (
        f"\n[score-coverage] {len(untested_sources)} file(s) not linked "
        f"into any test ({extra_lines_found} lines at 0% coverage) — "
        f"not reflected in the TOTALS above.\n"
    )
    return summary_text + banner


_UNTESTED_HTML_TEMPLATE = """<!doctype html>
<html>
<head>
  <meta charset='UTF-8'>
  <link rel='stylesheet' type='text/css' href='{css_path}'>
  <title>{title}</title>
</head>
<body>
<h2>Coverage Report</h2>
<h4>{title}</h4>
<p style='background:#fdd;padding:10px;border-radius:5px;border:1px solid #c66;'>
<strong>Not linked into any test.</strong> This source file is reachable from
the configured coverage targets but no test binary instruments it, so every
line is reported as uncovered.
</p>
<table>
<tr><td class='code'><pre>
{body}
</pre></td></tr>
</table>
</body>
</html>
"""


def _augment_html_with_untested(
    html_report_dir: Path,
    untested_sources: List[str],
    workspace_root: str,
) -> None:
    """Create per-file HTML pages for untested sources and link them from index.

    The pages are intentionally minimal: llvm-cov's per-source HTML format is
    not easily reproducible without the full coverage mapping, so we render a
    plain source dump with a banner that explains the file was not exercised.
    The index page gets a new "Not Linked Into Tests" section listing the
    files at 0% coverage so the gap is visible to reviewers.
    """
    if not html_report_dir.exists():
        return

    coverage_subdir = html_report_dir / "coverage"
    output_root = coverage_subdir if coverage_subdir.exists() else html_report_dir

    entries: List[Tuple[str, str, int]] = []  # (rel_source, href, num_lines)
    for abs_path in untested_sources:
        rel_source = os.path.relpath(abs_path, workspace_root)
        # Mirror llvm-cov: per-source HTML lives at <output_root>/<abs path>.html
        # Strip the leading "/" so that the path joins under output_root.
        target_html = output_root / (abs_path.lstrip("/") + ".html")
        target_html.parent.mkdir(parents=True, exist_ok=True)

        try:
            with open(abs_path, "r", encoding="utf-8", errors="replace") as f:
                source_text = f.read()
        except OSError:
            continue
        num_lines = source_text.count("\n") + (
            0 if source_text.endswith("\n") or not source_text else 1
        )

        rel_to_root = os.path.relpath(html_report_dir, target_html.parent)
        css_path = (Path(rel_to_root) / "style.css").as_posix()
        body = _escape_html(source_text) or "(empty file)"
        html = (
            _UNTESTED_HTML_TEMPLATE
            .replace("{css_path}", css_path)
            .replace("{title}", _escape_html(rel_source))
            .replace("{body}", body)
        )
        target_html.write_text(html, encoding="utf-8")

        href = target_html.relative_to(html_report_dir).as_posix()
        entries.append((rel_source, href, max(num_lines, 1)))

    if not entries:
        return

    _inject_untested_section_into_index(html_report_dir / "index.html", entries)


def _escape_html(text: str) -> str:
    return (
        text.replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace("'", "&#39;")
        .replace('"', "&quot;")
    )


def _inject_untested_section_into_index(
    index_file: Path, entries: List[Tuple[str, str, int]]
) -> None:
    """Insert a banner + table of untested files into the llvm-cov index page."""
    if not index_file.exists():
        return

    content = index_file.read_text(encoding="utf-8")

    rows = []
    for rel_source, href, num_lines in entries:
        rows.append(
            "<tr class='light-row'>"
            f"<td><pre><a href='{_escape_html(href)}'>{_escape_html(rel_source)}</a></pre></td>"
            f"<td class='column-entry-red'><pre>  0.00% (0/{num_lines})</pre></td>"
            "<td class='column-entry-red'><pre>Not linked into any test</pre></td>"
            "</tr>"
        )

    section = (
        "<div style='background:#fdd;padding:10px;margin:10px 0;border-radius:5px;"
        "border:1px solid #c66;'>"
        f"<strong>{len(entries)} file(s) not linked into any test "
        "(counted as 0% coverage).</strong>"
        "</div>"
        "<h3>Not Linked Into Tests</h3>"
        "<table><tr><td class='column-entry-bold'>Filename</td>"
        "<td class='column-entry-bold'>Line Coverage</td>"
        "<td class='column-entry-bold'>Note</td></tr>"
        f"{''.join(rows)}</table>"
    )

    if "</body>" in content:
        content = content.replace("</body>", section + "</body>", 1)
    else:
        content += section

    index_file.write_text(content, encoding="utf-8")


def parse_args() -> argparse.Namespace:
    """Parse command-line arguments matching the Bazel coverage_report_generator interface."""
    parser = argparse.ArgumentParser(description="LLVM coverage reporter for Bazel")
